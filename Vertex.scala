

class Vertex ( xAxis : Int, yAxis : Int) {
    private var x : Int = xAxis;
    private var y : Int = yAxis;
    
    def getX () : Int = {
      return x;
    }
    def setX (newX : Int) {
      x = newX;
    }
    
    def getY () : Int = {
      return y;
    }
    def setY (newY : Int) {
      y = newY;
    }
    
}